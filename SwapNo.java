import java.util.*;
class SwapNo
{
public static void main(String args[])
{
int x=20;
int y=10;

System.out.println("Before swapping the numbers:");
System.out.println("The value of x:"+x);
System.out.println("The value of y:"+y);

x=x-y;
y=x+y;


System.out.println("After swapping the numbers:");
System.out.println("The value of x:"+x);
System.out.println("The value of y:"+y);
}
}